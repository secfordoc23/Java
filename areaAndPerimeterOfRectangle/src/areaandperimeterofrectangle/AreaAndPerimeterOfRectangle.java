//Purpose: This program is designed to display the area and perimeter
//  of a rectangle using the given data:
//  Length: 7.9 Width: 4.5 and Formulas: area = length x width       

package areaandperimeterofrectangle;

public class AreaAndPerimeterOfRectangle 
{
    //===========  main ==========================
    public static void main(String[] args) 
    {
  
        // Display calculated Area and Perimeter
        System.out.println("Area of the Rectangle: ");
        System.out.println(7.9 * 4.5);
        System.out.println("Perimeter of the Rectangle: ");
        System.out.println((2 * 7.9) + (2 * 4.5));
    }

}
